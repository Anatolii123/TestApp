CREATE TABLE PERSON
(
    ID INT NOT NULL PRIMARY KEY,
    NAME VARCHAR(45) NOT NULL,
    BIRTH_DATE DATE NOT NULL
);

CREATE TABLE CAR
(
    ID INT NOT NULL PRIMARY KEY,
    MODEL VARCHAR(45),
    HORSEPOWER INT NOT NULL,
    OWNER_ID INT NOT NULL,
    FOREIGN KEY (OWNER_ID) REFERENCES PERSON(ID)
);